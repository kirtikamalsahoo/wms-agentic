// Utility function to get a random image from an array
export const getRandomImage = (imageArray) => {
  if (!Array.isArray(imageArray) || imageArray.length === 0) {
    return productImages.default;
  }
  return imageArray[Math.floor(Math.random() * imageArray.length)];
};

// Get image by category with random selection
export const getImageByCategory = (category) => {
  const categoryKey = category.toLowerCase();
  
  if (productImages[categoryKey]) {
    return Array.isArray(productImages[categoryKey]) 
      ? getRandomImage(productImages[categoryKey])
      : productImages[categoryKey];
  }
  
  return productImages.default;
};

// Get specific product image by exact product name match
export const getSpecificProductImage = (productName) => {
  if (!productName) return null;
  
  const normalizedName = productName.toLowerCase().trim();
  
  // Check for exact match first
  if (productImages.specificProducts[normalizedName]) {
    return productImages.specificProducts[normalizedName];
  }
  
  // Check for partial matches
  for (const [key, image] of Object.entries(productImages.specificProducts)) {
    if (normalizedName.includes(key) || key.includes(normalizedName)) {
      return image;
    }
  }
  
  return null;
};

